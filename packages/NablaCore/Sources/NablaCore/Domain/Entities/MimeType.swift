import Foundation

public enum MimeType: String {
    // Image
    case png = "image/png"
    case jpg = "image/jpeg"
    case heic = "image/heic"
    case heif = "image/heif"

    // Video
    case mov = "video/mov"

    // Audio
    case mpeg = "audio/mpeg"

    // Document
    case pdf = "application/pdf"
}
